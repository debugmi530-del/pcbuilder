def run(cmd):
    result = subprocess.run(cmd, shell=True, capture_output=True, text=True)
    print(result.stdout)
    if result.returncode != 0:
        print("STDERR:", result.stderr)
    return result.returncode

for wave_num, (wave_label, gpus) in enumerate(WAVES, 1):
    print(f"\n{'='*60}")
    print(f"  {wave_label}")
    print(f"{'='*60}")

    content = read_file()
    # Убрать последнюю строку '];\n' и вставить новые GPU
    marker = "];"
    idx = content.rfind(marker)
    if idx == -1:
        print("ERROR: не найден маркер '];'")
        break

    dart_block = "\n".join(make_dart(g) for g in gpus)
    new_content = content[:idx] + dart_block + "\n\n];"
    write_file(new_content)

    ids = ", ".join(g["id"] for g in gpus)
    brands = ", ".join(g["brand"] for g in gpus)
    commit_msg = f"{wave_label}: {ids} ({brands})"

    rc = run(f'git add lib/data/gpu_components.dart && git commit -m "{commit_msg}" && git push origin main')
    if rc != 0:
        print(f"PUSH FAILED at wave {wave_num}, trying force...")
        run(f'git push origin main --force')

    print(f"Волна {wave_num}/10 готова. Добавлено: {ids}")

print("\nВсе 10 волн завершены. Всего добавлено 50 исполнений (gpu-046..gpu-095).")
